#include <iostream>
using namespace std;

int main() {

    int a, b, c;
    int nilai[2][3][3] =
    {
        {
            {120, 283, 350},
            {450, 246, 365},
            {650, 342, 461}
        },
        {
            {188, 251, 357},
            {245, 370, 478},
            {320, 484, 589}
        }
    };

    int m = 1;

    cout << "----------------------------------------------" << endl;
    cout << "|                Tahun Penjualan             |" << endl;
    cout << "----------------------------------------------" << endl;
    cout << "| Tahun ke | Hasil ke |  2002 |  2003 |  2004 |" << endl;
    cout << "----------------------------------------------" << endl;

    for (a = 0; a < 2; a++) {
        for (b = 0; b < 3; b++) {
            cout << "|    " << m << "     |    " << b + 1 << "     | ";
            for (c = 0; c < 3; c++) {
                cout << nilai[a][b][c] << "  | ";
            }
            cout << endl;
        }
        m++;
        cout << "----------------------------------------------" << endl;
    }

}
